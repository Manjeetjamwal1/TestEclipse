package Day03_07;

public class PositiveNegativeNumber {

	public static void main(String[] args) {
		int num = 0;
		if (num > 0) {
			System.out.println(num + " is Positive");

		} else if (num < 0) {
			System.out.println(num + " is Negative");

		} else {
			System.out.println("Number is Zero");
		}

	}

}
